package ljx.ashin.reflect;

/**
 * 提供给反射用的类
 * Created by Ashin Liang on 2017/12/18.
 */
public class Person {

    private Integer id;
    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
